    public void LoadContent(){
    
        try {
                 ResultSet vehiclers= DbConnection.execute("SELECT * FROM `vehicle` INNER JOIN `fuel_type` ON `vehicle`.`fuel_type_id`=`fuel_type`.`id`");
          DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        while (vehiclers.next()) {
            Vector<Object> vehicleRow = new Vector<>();
            vehicleRow.add(vehiclers.getString("vehicle_number"));
            vehicleRow.add(vehiclers.getString("chassis_number"));
            vehicleRow.add(vehiclers.getString("type"));
            model.addRow(vehicleRow);
        }
        } catch (Exception e) {
            e.printStackTrace();
        }
    
    }

