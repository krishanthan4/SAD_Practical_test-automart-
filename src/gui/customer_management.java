    public void LoadContent(){
    
        try {
                 ResultSet vehiclers= DbConnection.execute("SELECT *,city.name AS city_name FROM `customer` INNER JOIN customer_has_address ON `customer`.`mobile`=`customer_has_address`.`customer_mobile` INNER JOIN `address` ON `address`.`address_id`=`customer_has_address`.`address_address_id` INNER JOIN `gender` ON `gender`.`id`=`customer`.`gender_id` INNER JOIN `customer_type` ON `customer`.`customer_type_id`=`customer_type`.`id` INNER JOIN `city` ON `city`.`city_id`=`address`.`city_city_id`");
          DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        while (vehiclers.next()) {
            Vector<Object> vehicleRow = new Vector<>();
            vehicleRow.add(vehiclers.getString("mobile"));
            vehicleRow.add(vehiclers.getString("firstname") +"  "+vehiclers.getString("lastname"));
            vehicleRow.add(vehiclers.getString("line1") +" ,"+vehiclers.getString("line2") +" ,"+vehiclers.getString("city_name"));        
            vehicleRow.add(vehiclers.getString("name"));
                        vehicleRow.add(vehiclers.getString("type"));


            model.addRow(vehicleRow);
        }
        } catch (Exception e) {
            e.printStackTrace();
        }
    
    }
